public class Circle {
    private /* статик кайра жазылышы керек */ static final double PI=3.14;//өзгөрбөй турган свойства имитюбиль
    public static double area(int radius){
        return PI*(radius*radius); //1бизге берилген формула

    }
    public static double circumference(int radius ){
        return PI*2*radius; //2
    }






}
